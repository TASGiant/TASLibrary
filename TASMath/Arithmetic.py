def add(num1, num2):
    return num1 + num2


def subtract(num1, num2):
    return num1 - num2


def devide(num1, num2):
    return num1 / num2


def multiply(num1, num2):
    return num1 * num2


def remainder(num1, num2):
    return num1 % num2


class Print:

    @staticmethod
    def add(num1, num2):
        print(num1 + num2)

    @staticmethod
    def subtract(num1, num2):
        print(num1 - num2)

    @staticmethod
    def devide(num1, num2):
        print(num1 / num2)

    @staticmethod
    def multiply(num1, num2):
        print(num1 * num2)

    @staticmethod
    def remainder(num1, num2):
        print(num1 % num2)
